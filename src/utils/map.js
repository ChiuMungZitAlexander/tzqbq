export default {
	tzMap (value) {
		if (value.indexOf('兴化') !== -1) {
			return '兴化市'
		}
		if (value.indexOf('海陵') !== -1) {
			return '海陵区'
		}
		if (value.indexOf('姜堰') !== -1) {
			return '姜堰区'
		}
		if (value.indexOf('靖江') !== -1) {
			return '靖江市'
		}
		if (value.indexOf('高港') !== -1) {
			return '高港区'
		}
		if (value.indexOf('泰兴') !== -1) {
			return '泰兴市'
		}
		if (value.indexOf('医药高新') !== -1) {
			return '医药高新区'
		}
	}
}
